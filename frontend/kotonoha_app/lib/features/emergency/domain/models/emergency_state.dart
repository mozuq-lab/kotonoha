/// 緊急状態列挙型
///
/// TASK-0047: 緊急音・画面赤表示実装
/// 関連要件: REQ-303, REQ-304
/// 信頼性レベル: 青信号（要件定義書ベース）
///
/// 緊急呼び出し機能の状態を表す列挙型。
/// アプリケーション全体で緊急状態を追跡するために使用。
library;

/// 緊急呼び出し機能の状態
///
/// normal: 通常状態（緊急呼び出しなし）
/// alertActive: 緊急呼び出し中（音・画面表示あり）
/// resetting: リセット処理中（将来の拡張用）
enum EmergencyStateEnum {
  /// 通常状態
  ///
  /// 緊急呼び出しが実行されていない状態。
  /// アプリ起動時の初期状態。
  normal,

  /// 緊急呼び出し中
  ///
  /// 緊急音が再生され、画面が赤く表示されている状態。
  /// リセットボタンタップまで継続。
  alertActive,

  /// リセット処理中（将来の拡張用）
  ///
  /// リセットボタンタップ後の処理中状態。
  /// 現時点では使用しない。
  resetting,
}
